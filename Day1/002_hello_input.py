"""
A program that demonstrates basic user input in Python.
It asks for the user's name and then greets them personally.
"""

# Variables are containers for storing data values
# They can hold different types of data (text, numbers, etc.)
# Here, we create a variable called 'name' to store the user's input
name = input("Enter your name: ")

# We can then use this variable in other parts of our code
# Here we use it in our greeting message
print(f"Hello, {name}!")